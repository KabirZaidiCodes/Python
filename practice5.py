# Strings

name = "Insha"
sname = "Zaidi"
print(name + " " + sname)
# the above code is an example of a string

sen = ''' My name is Kabir,
 her name is Insha. 
 So cool everything is.'''
print(sen)
# the above code is an example of multiline string

print(name[0])
print(sname[2])
# the above code can be used to access a character of a string

# FOR LOOP ->
for character in sen:
    print(character)
# the above code is an example of a for loop
