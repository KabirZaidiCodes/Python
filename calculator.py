# THIS IS A CALCULATOR MADE WITH PYTHON

a = 11
b = 7

print("the value of", a, "+", b, "is:", a+b)
print("the value of", a, "-", b, "is:", a-b)
print("the value of", a, "*", b, "is:", a*b)
print("the value of", a, "/", b, "is:", a/b)

# this is how we make a simple calculator with python without using user's input
